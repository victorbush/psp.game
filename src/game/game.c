void game__construct(game_t* game, game_intf_t* intf)
{
	FATAL_IF_NULL(game);
	FATAL_IF_NULL(intf);

	clear_struct(game);
	game->intf = intf;

	game->intf->__construct(game);
}

void game__destruct(game_t* game)
{
	game->intf->__destruct(game);
}

